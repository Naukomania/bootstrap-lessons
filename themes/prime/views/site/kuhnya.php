 <p>Чем насыщеннее текстура и цвет, тем больше затрат на производство.  Самыми недорогими цветами являются светлые монотонные цвета, затем яркие монотонные, за ними следуют цвета с песчаной текстурой, потом идет градация по крупности  вкраплений и, наконец завершают ценовую цепочку цветов - камня с мраморной текстурой и / или включением 3D частиц (они же вкрапления, включения, крапушки). </p>
<p>Производители искусственного акрилового камня не рекомендуют заказывать кухонные столешницы из ярких и темных цветов. Такие цвета не являются износостойкими. При интенсивном использовании появляются царапины, оставляющие светлый след. Можно сравнить, ну очень сравнительно, как с лакированной темной мебелью. </p>
<p>Ежели дизайн и задуманный стиль не позволяют изменить цвет, то в данном случае можно посоветовать несколько вариантов:</p>
<p>- Сделать заказ столешницы из камня Corian компании Dupont – именно из коллекции Deep. Не так давно они изобрели новую технологию для цветов темного цвета – прокрашивать акрил в глубину. И в случае, если столешница поцарапается – вы не увидите  царапины, визуально сложно будет ее обнаружить.  К недостатку этого выбора можно отнести, лишь,  сравнительную стоимость камня,  а следовательно самого заказа по отношению к аналогичным цветам других производителей акрилового камня.</p>
<p>-  Сделать выбор с цвета с бОльшим числом вкраплений, они снивелируют  визуально видимость царапин,   и как можно бережнее относиться к  изделию. Также придется смириться с тем, что время от времени придется тратиться на  перешлифовку столешницы.  В данном варианте, можно будущего заказчика обнадежить тем, что столешницу из акрилового камня можно перешлифовывать и ремонтировать бесконечное количество раз. В этом случае, рекомендуется оставить у себя ремонтный кусочек камня для сильных повреждений (вроде скола). Очень важно, чтобы ремонтный кусочек камня был именно из этой же партии производства камня, иначе тон камня может не совпадать. Если не запасетесь, то через какое-то время, совершенно будет безнадежно искать нужную партию определенного цвета, нужного бренда, ситуация как с обоями.</p>
<p>В целом, могу сказать, что при достаточно среднем хозяйственном пользовании – поддерживание чистоты, не употребления едких химикатов и грубых абразивных средств для мытья, если не воздействовать на столешницу тяжелыми механическими ударами, не вставать на столешницу в тонких местах (например: в местах выреза под варочную поверхность и мойку) не пользоваться столешницей в качестве разделочной доски, а использовать оную, то столешница вам послужит многие годы.</p></details>


